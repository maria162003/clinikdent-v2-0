// Dashboard Admin JavaScript - Versi√≥n Corregida y Simplificada
console.log('üî• Iniciando Dashboard Admin Corregido...');

// Evitar m√∫ltiples instancias
if (window.dashboardAdminFixed) {
    console.log('‚ö†Ô∏è Dashboard Admin ya existe');
} else {
    window.dashboardAdminFixed = true;
    
    class DashboardAdmin {
        constructor() {
            console.log('üöÄ Construyendo DashboardAdmin...');
            this.currentSection = 'dashboard';
            this.users = [];
            this.citas = [];
            this.init();
        }

        // Obtener headers de autenticaci√≥n
        getAuthHeaders() {
            const user = JSON.parse(localStorage.getItem('user') || '{}');
            const userId = localStorage.getItem('userId') || user.id;
            
            return {
                'Content-Type': 'application/json',
                'user-id': userId || '1'
            };
        }

        // Hacer petici√≥n con autenticaci√≥n
        async authFetch(url, options = {}) {
            const headers = this.getAuthHeaders();
            
            return fetch(url, {
                ...options,
                headers: {
                    ...headers,
                    ...options.headers
                }
            });
        }

        init() {
            console.log('üîÑ Inicializando dashboard...');
            this.setupEventListeners();
            this.loadUserInfo();
            this.loadDashboardData();
        }

        setupEventListeners() {
            console.log('üîó Configurando event listeners...');
            
            // Navigation listeners
            document.querySelectorAll('[data-section]').forEach(link => {
                link.addEventListener('click', (e) => {
                    e.preventDefault();
                    const section = e.target.getAttribute('data-section');
                    this.showSection(section);
                });
            });
        }

        async loadUserInfo() {
            console.log('üë§ Cargando informaci√≥n de usuario...');
            
            try {
                let userData = localStorage.getItem('user');
                let user;
                
                if (!userData) {
                    console.log('üîß No hay datos de usuario, usando por defecto');
                    user = {
                        id: 1,
                        nombre: 'Administrador',
                        apellido: 'Sistema',
                        email: 'admin@clinikdent.com',
                        rol: 'administrador'
                    };
                    localStorage.setItem('user', JSON.stringify(user));
                    localStorage.setItem('userId', '1');
                } else {
                    user = JSON.parse(userData);
                }
                
                this.currentUser = user;
                
                const userNameElement = document.getElementById('userName');
                if (userNameElement) {
                    const nombreCompleto = `${user.nombre || ''} ${user.apellido || ''}`.trim();
                    userNameElement.textContent = nombreCompleto || 'Administrador';
                    console.log('‚úÖ Usuario cargado:', nombreCompleto);
                }
            } catch (error) {
                console.error('‚ùå Error cargando usuario:', error);
                const userNameElement = document.getElementById('userName');
                if (userNameElement) {
                    userNameElement.textContent = 'Administrador';
                }
            }
        }

        async loadDashboardData() {
            console.log('üìä Cargando datos del dashboard...');
            
            try {
                // Cargar usuarios
                const usersResponse = await this.authFetch('/api/usuarios');
                if (usersResponse.ok) {
                    this.users = await usersResponse.json();
                    console.log('‚úÖ Usuarios cargados:', this.users.length);
                }
                
                // Cargar citas
                const citasResponse = await this.authFetch('/api/citas/obtener-todas');
                if (citasResponse.ok) {
                    this.citas = await citasResponse.json();
                    console.log('‚úÖ Citas cargadas:', this.citas.length);
                }
                
                // Actualizar contadores del dashboard
                this.updateDashboardCounters();
                
            } catch (error) {
                console.error('‚ùå Error cargando datos:', error);
            }
        }

        updateDashboardCounters() {
            console.log('üî¢ Actualizando contadores...');
            
            // Actualizar contadores b√°sicos
            const totalUsuarios = document.getElementById('totalUsuarios');
            if (totalUsuarios) {
                totalUsuarios.textContent = this.users.length || 0;
            }
            
            const totalCitas = document.getElementById('totalCitas');
            if (totalCitas) {
                totalCitas.textContent = this.citas.length || 0;
            }
            
            console.log('‚úÖ Contadores actualizados');
        }

        showSection(section) {
            console.log('üîÑ Cambiando a secci√≥n:', section);
            this.currentSection = section;
            
            // Ocultar todas las secciones
            document.querySelectorAll('.admin-section').forEach(sec => {
                sec.style.display = 'none';
            });
            
            // Mostrar la secci√≥n seleccionada
            const targetSection = document.getElementById(`section-${section}`);
            if (targetSection) {
                targetSection.style.display = 'block';
                
                // Cargar datos espec√≠ficos de la secci√≥n
                this.loadSectionData(section);
            }
            
            // Actualizar navegaci√≥n activa
            document.querySelectorAll('.nav-link').forEach(link => {
                link.classList.remove('active');
            });
            
            const activeLink = document.querySelector(`[data-section="${section}"]`);
            if (activeLink) {
                activeLink.classList.add('active');
            }
        }

        async loadSectionData(section) {
            console.log('üì¶ Cargando datos para secci√≥n:', section);
            
            switch (section) {
                case 'usuarios':
                    await this.loadUsersSection();
                    break;
                case 'inventario':
                    await this.loadInventorySection();
                    break;
                case 'pagos':
                    await this.loadPaymentsSection();
                    break;
                case 'reportes':
                    await this.loadReportsSection();
                    break;
                default:
                    console.log('üè† Secci√≥n dashboard por defecto');
            }
        }

        async loadUsersSection() {
            console.log('üë• Cargando secci√≥n usuarios...');
            try {
                const response = await this.authFetch('/api/usuarios');
                if (response.ok) {
                    const users = await response.json();
                    this.renderUsersTable(users);
                }
            } catch (error) {
                console.error('‚ùå Error cargando usuarios:', error);
            }
        }

        async loadInventorySection() {
            console.log('üì¶ Cargando secci√≥n inventario...');
            try {
                const response = await this.authFetch('/api/inventario');
                if (response.ok) {
                    const inventory = await response.json();
                    console.log('‚úÖ Inventario cargado');
                }
            } catch (error) {
                console.error('‚ùå Error cargando inventario:', error);
            }
        }

        async loadPaymentsSection() {
            console.log('üí∞ Cargando secci√≥n pagos...');
            try {
                const response = await this.authFetch('/api/pagos');
                if (response.ok) {
                    const payments = await response.json();
                    console.log('‚úÖ Pagos cargados');
                }
            } catch (error) {
                console.error('‚ùå Error cargando pagos:', error);
            }
        }

        async loadReportsSection() {
            console.log('üìä Cargando secci√≥n reportes...');
            try {
                const response = await this.authFetch('/api/reportes/resumen-general');
                if (response.ok) {
                    const reports = await response.json();
                    console.log('‚úÖ Reportes cargados');
                }
            } catch (error) {
                console.error('‚ùå Error cargando reportes:', error);
            }
        }

        renderUsersTable(users) {
            console.log('üîÑ Renderizando tabla de usuarios...');
            const tableBody = document.getElementById('usersTableBody');
            if (!tableBody) return;
            
            tableBody.innerHTML = users.map(user => `
                <tr>
                    <td>${user.id}</td>
                    <td>${user.nombre || ''} ${user.apellido || ''}</td>
                    <td>${user.email || ''}</td>
                    <td>${user.rol || ''}</td>
                    <td>
                        <button class="btn btn-sm btn-primary" onclick="editUser(${user.id})">
                            Editar
                        </button>
                    </td>
                </tr>
            `).join('');
            
            console.log('‚úÖ Tabla de usuarios renderizada');
        }
    }

    // Funciones globales auxiliares
    window.editUser = function(userId) {
        console.log('‚úèÔ∏è Editando usuario:', userId);
        alert(`Editando usuario ID: ${userId}`);
    };

    // Inicializar cuando el DOM est√© listo
    document.addEventListener('DOMContentLoaded', () => {
        console.log('üèÅ DOM listo, iniciando dashboard...');
        
        if (!window.dashboardAdminInstance) {
            window.dashboardAdminInstance = new DashboardAdmin();
            console.log('‚úÖ Dashboard Admin inicializado');
        } else {
            console.log('‚ö†Ô∏è Dashboard ya existe');
        }
    });
}

console.log('üéØ Archivo dashboard-admin-fixed.js cargado');
