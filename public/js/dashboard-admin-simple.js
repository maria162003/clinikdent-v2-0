// Dashboard Admin JavaScript - Versi√≥n Funcional Simplificada
console.log('üî• Iniciando Dashboard Admin...');

// Variable global para evitar m√∫ltiples inicializaciones
window.dashboardInitialized = false;

class DashboardAdmin {
    constructor() {
        if (window.dashboardInitialized) {
            console.log('‚ö†Ô∏è Dashboard ya inicializado');
            return;
        }
        
        console.log('üöÄ Construyendo DashboardAdmin...');
        window.dashboardInitialized = true;
        
        this.currentSection = 'dashboard';
        this.users = [];
        this.citas = [];
        this.currentUser = null;
        
        this.init();
    }

    getAuthHeaders() {
        const user = JSON.parse(localStorage.getItem('user') || '{}');
        const userId = localStorage.getItem('userId') || user.id;
        
        return {
            'Content-Type': 'application/json',
            'user-id': userId || '1'
        };
    }

    async authFetch(url, options = {}) {
        const headers = this.getAuthHeaders();
        
        return fetch(url, {
            ...options,
            headers: {
                ...headers,
                ...options.headers
            }
        });
    }

    init() {
        console.log('üîÑ Inicializando dashboard...');
        this.loadUserInfo();
        this.setupEventListeners();
        this.loadDashboardData();
    }

    async loadUserInfo() {
        console.log('üë§ Cargando informaci√≥n de usuario...');
        
        try {
            let userData = localStorage.getItem('user');
            let user;
            
            if (!userData) {
                console.log('üîß Creando usuario por defecto');
                user = {
                    id: 1,
                    nombre: 'Administrador',
                    apellido: 'Sistema',
                    email: 'admin@clinikdent.com',
                    rol: 'administrador'
                };
                localStorage.setItem('user', JSON.stringify(user));
                localStorage.setItem('userId', '1');
            } else {
                user = JSON.parse(userData);
            }
            
            this.currentUser = user;
            
            // Actualizar el nombre en la interfaz
            const userNameElement = document.getElementById('userName');
            if (userNameElement) {
                const nombreCompleto = `${user.nombre || ''} ${user.apellido || ''}`.trim();
                userNameElement.textContent = nombreCompleto || 'Administrador';
                console.log('‚úÖ Nombre de usuario actualizado:', nombreCompleto);
            } else {
                console.warn('‚ö†Ô∏è Elemento userName no encontrado');
            }
            
            // Actualizar rol si existe
            const userRoleElement = document.getElementById('userRole');
            if (userRoleElement) {
                userRoleElement.textContent = user.rol || 'Administrador';
            }
            
        } catch (error) {
            console.error('‚ùå Error cargando usuario:', error);
            // Fallback seguro
            const userNameElement = document.getElementById('userName');
            if (userNameElement) {
                userNameElement.textContent = 'Administrador';
            }
        }
    }

    setupEventListeners() {
        console.log('üîó Configurando event listeners...');
        
        // Manejar clicks en navegaci√≥n
        document.querySelectorAll('.nav-link[data-section]').forEach(link => {
            link.addEventListener('click', (e) => {
                e.preventDefault();
                const section = e.target.closest('[data-section]').getAttribute('data-section');
                this.showSection(section);
            });
        });

        // Manejar clicks en botones de secci√≥n
        document.querySelectorAll('[onclick*="showSection"]').forEach(element => {
            element.addEventListener('click', (e) => {
                e.preventDefault();
                const onclick = element.getAttribute('onclick');
                const match = onclick.match(/showSection\('([^']+)'\)/);
                if (match) {
                    this.showSection(match[1]);
                }
            });
        });
    }

    async loadDashboardData() {
        console.log('üìä Cargando datos del dashboard...');
        
        try {
            // Cargar estad√≠sticas b√°sicas
            await Promise.all([
                this.loadUsers(),
                this.loadCitas(),
                this.loadInventory(),
                this.loadPayments()
            ]);
            
            this.updateDashboardCounters();
            console.log('‚úÖ Datos del dashboard cargados');
            
        } catch (error) {
            console.error('‚ùå Error cargando datos del dashboard:', error);
        }
    }

    async loadUsers() {
        try {
            const response = await this.authFetch('/api/usuarios');
            if (response.ok) {
                this.users = await response.json();
                console.log('‚úÖ Usuarios cargados:', this.users.length);
            }
        } catch (error) {
            console.error('‚ùå Error cargando usuarios:', error);
            this.users = [];
        }
    }

    async loadCitas() {
        try {
            const response = await this.authFetch('/api/citas/obtener-todas');
            if (response.ok) {
                this.citas = await response.json();
                console.log('‚úÖ Citas cargadas:', this.citas.length);
            }
        } catch (error) {
            console.error('‚ùå Error cargando citas:', error);
            this.citas = [];
        }
    }

    async loadInventory() {
        try {
            const response = await this.authFetch('/api/inventario');
            if (response.ok) {
                const inventory = await response.json();
                this.inventory = inventory;
                console.log('‚úÖ Inventario cargado');
            }
        } catch (error) {
            console.error('‚ùå Error cargando inventario:', error);
            this.inventory = { equipos: [] };
        }
    }

    async loadPayments() {
        try {
            const response = await this.authFetch('/api/pagos');
            if (response.ok) {
                this.payments = await response.json();
                console.log('‚úÖ Pagos cargados:', this.payments.length);
            }
        } catch (error) {
            console.error('‚ùå Error cargando pagos:', error);
            this.payments = [];
        }
    }

    updateDashboardCounters() {
        console.log('üî¢ Actualizando contadores...');
        
        // Actualizar contadores en el dashboard
        const updates = [
            { id: 'totalUsuarios', value: this.users.length || 0 },
            { id: 'totalCitas', value: this.citas.length || 0 },
            { id: 'totalPagos', value: this.payments?.length || 0 },
            { id: 'totalInventario', value: this.inventory?.equipos?.length || 0 }
        ];

        updates.forEach(({ id, value }) => {
            const element = document.getElementById(id);
            if (element) {
                element.textContent = value;
                console.log(`‚úÖ Actualizado ${id}: ${value}`);
            }
        });
    }

    showSection(section) {
        console.log('üîÑ Cambiando a secci√≥n:', section);
        this.currentSection = section;
        
        // Ocultar todas las secciones
        document.querySelectorAll('.admin-section').forEach(sec => {
            sec.style.display = 'none';
        });
        
        // Mostrar la secci√≥n seleccionada
        const targetSection = document.getElementById(`section-${section}`);
        if (targetSection) {
            targetSection.style.display = 'block';
            
            // Cargar datos espec√≠ficos de la secci√≥n
            this.loadSectionData(section);
        } else {
            console.warn(`‚ö†Ô∏è Secci√≥n no encontrada: section-${section}`);
        }
        
        // Actualizar navegaci√≥n activa
        document.querySelectorAll('.nav-link').forEach(link => {
            link.classList.remove('active');
        });
        
        const activeLink = document.querySelector(`[data-section="${section}"]`);
        if (activeLink) {
            activeLink.classList.add('active');
        }
    }

    async loadSectionData(section) {
        console.log('üì¶ Cargando datos para secci√≥n:', section);
        
        try {
            switch (section) {
                case 'usuarios':
                    await this.loadUsersSection();
                    break;
                case 'inventario':
                    await this.loadInventorySection();
                    break;
                case 'pagos':
                    await this.loadPaymentsSection();
                    break;
                case 'reportes':
                    await this.loadReportsSection();
                    break;
                case 'configuracion':
                    await this.loadConfigSection();
                    break;
                default:
                    console.log('üè† Secci√≥n dashboard por defecto');
            }
        } catch (error) {
            console.error(`‚ùå Error cargando secci√≥n ${section}:`, error);
        }
    }

    async loadUsersSection() {
        console.log('üë• Cargando secci√≥n usuarios...');
        // La l√≥gica espec√≠fica de usuarios se manejar√° con los m√≥dulos existentes
    }

    async loadInventorySection() {
        console.log('üì¶ Cargando secci√≥n inventario...');
        // La l√≥gica espec√≠fica de inventario se manejar√° con los m√≥dulos existentes
    }

    async loadPaymentsSection() {
        console.log('üí∞ Cargando secci√≥n pagos...');
        // La l√≥gica espec√≠fica de pagos se manejar√° con los m√≥dulos existentes
    }

    async loadReportsSection() {
        console.log('üìä Cargando secci√≥n reportes...');
        try {
            const response = await this.authFetch('/api/reportes/resumen-general');
            if (response.ok) {
                const reports = await response.json();
                console.log('‚úÖ Reportes cargados');
            }
        } catch (error) {
            console.error('‚ùå Error cargando reportes:', error);
        }
    }

    async loadConfigSection() {
        console.log('‚öôÔ∏è Cargando secci√≥n configuraci√≥n...');
        try {
            const response = await this.authFetch('/api/configuracion/sistema');
            if (response.ok) {
                const config = await response.json();
                console.log('‚úÖ Configuraci√≥n cargada');
            }
        } catch (error) {
            console.error('‚ùå Error cargando configuraci√≥n:', error);
        }
    }
}

// Funci√≥n global para compatibilidad
window.showSection = function(section) {
    if (window.dashboardInstance) {
        window.dashboardInstance.showSection(section);
    } else {
        console.error('‚ùå Dashboard no inicializado');
    }
};

// Inicializar cuando el DOM est√© listo
document.addEventListener('DOMContentLoaded', () => {
    console.log('üèÅ DOM listo, iniciando dashboard...');
    
    if (!window.dashboardInstance) {
        window.dashboardInstance = new DashboardAdmin();
        console.log('‚úÖ Dashboard Admin inicializado exitosamente');
    } else {
        console.log('‚ö†Ô∏è Dashboard ya existe');
    }
});

